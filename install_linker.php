<h2>What is the Linker!?</h2>
<div>
<p>The Linker! allows you to easily create hyperlinks anywhere there is a textarea on the page. For example, in Yahoo! Answers, you can use the Linker! to quickly add references to your answer.</p>

<p>Without the Linker!, you would perform an external search, copy the link onto the clipboard, go back to your page and insert the link. In the case of blogging, sometimes this meant actually typing in the HTML code for an HTML anchor (&lt;a&gt;&lt;a&gt;)</p>

<p>The Linker! makes it much simpler. Just select text, hit the Linker! and it goes out and finds the best matches from Yahoo! Search and your Delicious bookmarks and automatically replaces your text with either the HTML code for an anchor or the URL directly. Also the Linker! is keyboard driven. You can hit the 'Enter' key to accept the change and dismiss the search overlay. Or cycle through results with the arrow keys. Hit 'ESC' to go back to original text and dismiss the overlay. Continue to cycle through results with the 'ESC' key.</p>

